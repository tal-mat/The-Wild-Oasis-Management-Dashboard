// Prepares data for the Pie chart based on confirmed stays
function prepareData(startData, stays) {
  // Increments the value of a specified field in the array of objects
  function incArrayValue(arr, field) {
    return arr.map((obj) =>
      obj.duration === field ? { ...obj, value: obj.value + 1 } : obj,
    );
  }

  // Reduces confirmed stays into structured data for the chart
  const data = stays
    .reduce((arr, cur) => {
      const num = cur.numNights;
      if (num === 1) return incArrayValue(arr, "1 night");
      if (num === 2) return incArrayValue(arr, "2 nights");
      if (num === 3) return incArrayValue(arr, "3 nights");
      if ([4, 5].includes(num)) return incArrayValue(arr, "4-5 nights");
      if ([6, 7].includes(num)) return incArrayValue(arr, "6-7 nights");
      if (num >= 8 && num <= 14) return incArrayValue(arr, "8-14 nights");
      if (num >= 15 && num <= 21) return incArrayValue(arr, "15-21 nights");
      if (num >= 21) return incArrayValue(arr, "21+ nights");
      return arr;
    }, startData)
    .filter((obj) => obj.value > 0); // Filter out entries with zero value

  return data;
}

// Renders the duration chart using the provided confirmed stays
function DurationChart({ confirmedStays }) {
  const { isDarkMode } = useDarkMode();
  const startData = isDarkMode ? startDataDark : startDataLight;
  const data = prepareData(startData, confirmedStays);

  return (
    // Styled container for displaying the stay duration summary chart
    <ChartBox>
      {/* Heading - displays the title for the stay duration summary chart */}
      <Heading as="h2">Stay duration summary</Heading>

      {/* ResponsiveContainer - adjusts the pie chart dimensions based on the parent container size */}
      <ResponsiveContainer width="100%" height={240}>
        {/* PieChart - renders a pie chart to visualize stay duration data */}
        <PieChart>
          {/* Pie - main data representation for stay durations; specifies the inner and outer radii, center position, and padding between segments */}
          <Pie
            data={data} // Data for the pie chart, using 'duration' as names and 'value' for segment values
            nameKey="duration"
            dataKey="value"
            innerRadius={85}
            outerRadius={110}
            cx="40%"
            cy="50%"
            paddingAngle={3}
          >
            {/* Cell - renders individual pie chart segments with corresponding colors for each duration */}
            {data.map((entry) => (
              <Cell
                fill={entry.color}
                stroke={entry.color}
                key={entry.duration}
              />
            ))}
          </Pie>

          {/* Tooltip - displays detailed information about each segment when hovered */}
          <Tooltip />

          {/* Legend - indicates the color-coded segments of the pie chart for easy identification */}
          <Legend
            verticalAlign="middle"
            align="right"
            width="30%"
            layout="vertical"
            iconSize={15}
            iconType="circle"
          />
        </PieChart>
      </ResponsiveContainer>
    </ChartBox>
  );
}

